package d20201130;

// 4. 논리형 : boolean (참, 거짓)

public class VarEx04 {                               // 클래스 시작
	public static void main(String[] args) {         // 메인 시작
		boolean bl1 = true;                          // boolean 자료형 bl1 변수에 true 대입
		boolean bl2 = false;                         // boolean 자료형 bl2 변수에 false 대입
		
		System.out.println("bl1 : "+ bl1);           // "bl1 : "+ bl1 출력
		System.out.println("bl2 : "+ bl2);           // "bl2 : "+ bl2 출력
		
		System.out.println("--------------------------");   // "--------------------------" 출력
		
		System.out.println(2 < 1);                   // 2 < 1에 대한 값 출력
		
		
		
	}                                                // 메인 끝

}                                                    // 클래스 끝
