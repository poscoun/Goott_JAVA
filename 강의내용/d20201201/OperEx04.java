package d20201201;

// 4. 비교 연산자
public class OperEx04 {		// 클래스 시작	
	public static void main(String[] args) {		// 메인 시작
		int a = 20;		// int 자료형 a 변수에 20 대입
		int b = 10;		// int 자료형 b 변수에 10 대입
		
		System.out.println(" a > b : "+ (a > b));		// " a > b : "+ (a > b) 출력
		System.out.println(" a >= b : "+ (a >= b));		// " a >= b : "+ (a >= b) 출력	
		System.out.println(" a < b : "+ (a < b));		// " a < b : "+ (a < b) 출력
		System.out.println(" a <= b : "+ (a <= b));		// " a <= b : "+ (a <= b) 출력
		System.out.println(" a == b : "+ (a == b));		// " a == b : "+ (a == b) 출력
		System.out.println(" a != b : "+ (a != b));		// " a != b : "+ (a != b) 출력
	}		// 메인 끝

}		// 클래스 끝
